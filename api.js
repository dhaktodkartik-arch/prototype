const API_KEYS_STORAGE_PREFIX = "srishti_api_key_";

// Helper functions to manage API keys in localStorage
function getStoredApiKey(provider) {
    return localStorage.getItem(`${API_KEYS_STORAGE_PREFIX}${provider}`) || "";
}

function setStoredApiKey(provider, key) {
    if (key.trim() === "") {
        localStorage.removeItem(`${API_KEYS_STORAGE_PREFIX}${provider}`);
    } else {
        localStorage.setItem(`${API_KEYS_STORAGE_PREFIX}${provider}`, key.trim());
    }
}

function hasAnyApiKey() {
    const providers = ["gemini", "openrouter", "openai", "anthropic", "grok", "perplexity"];
    return providers.some(p => getStoredApiKey(p) !== "");
}

// Multi-provider request router
async function callAgentAPI(agent, userPrompt, contextHistory = "") {
    const provider = agent.provider;
    const model = agent.model;
    const systemPrompt = agent.prompt;
    const apiKey = getStoredApiKey(provider);

    if (!apiKey) {
        // No key for this provider, generate context-aware simulation response
        return await generateSimulatedResponse(agent, userPrompt, contextHistory);
    }

    try {
        switch (provider) {
            case "gemini":
                return await callGeminiAPI(model, systemPrompt, userPrompt, contextHistory, apiKey);
            case "openrouter":
                return await callOpenRouterAPI(model, systemPrompt, userPrompt, contextHistory, apiKey);
            case "openai":
                return await callOpenAIAPI(model, systemPrompt, userPrompt, contextHistory, apiKey);
            case "anthropic":
                return await callAnthropicAPI(model, systemPrompt, userPrompt, contextHistory, apiKey);
            case "grok":
                return await callGrokAPI(model, systemPrompt, userPrompt, contextHistory, apiKey);
            case "perplexity":
                return await callPerplexityAPI(model, systemPrompt, userPrompt, contextHistory, apiKey);
            default:
                throw new Error(`Unknown provider: ${provider}`);
        }
    } catch (error) {
        console.warn(`API call failed for ${provider}, falling back to simulation:`, error);
        // Gracefully fall back to simulation instead of crashing the debate
        return await generateSimulatedResponse(agent, userPrompt, contextHistory);
    }
}

// 1. Google Gemini API Call
async function callGeminiAPI(model, systemPrompt, userPrompt, contextHistory, apiKey) {
    const url = `https://generativelanguage.googleapis.com/v1beta/models/${model}:generateContent?key=${apiKey}`;
    const promptText = `Context History of other agents' discussion:\n${contextHistory || "No previous discussion yet."}\n\nUser Question/Task:\n${userPrompt}`;
    
    const response = await fetch(url, {
        method: "POST",
        headers: {
            "Content-Type": "application/json"
        },
        body: JSON.stringify({
            contents: [
                {
                    role: "user",
                    parts: [{ text: promptText }]
                }
            ],
            systemInstruction: {
                parts: [{ text: systemPrompt }]
            },
            generationConfig: {
                temperature: 0.7,
                maxOutputTokens: 2048
            }
        })
    });

    if (!response.ok) {
        const errData = await response.json().catch(() => ({}));
        throw new Error(errData.error?.message || `HTTP ${response.status}`);
    }

    const data = await response.json();
    return data.candidates?.[0]?.content?.parts?.[0]?.text || "No response generated by Gemini.";
}

