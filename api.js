async function generateSimulatedResponse(agent, userPrompt, contextHistory) {
    // Artificial typing delay to simulate reasoning
    await new Promise(resolve => setTimeout(resolve, 1500 + Math.random() * 1000));

    const prompt = userPrompt.toLowerCase();
    
    // 1. Classify the user prompt
    let category = "general";
    if (/\b(code|website|html|css|js|javascript|python|java|c\+\+|bug|compile|git|api|database|sql|function|react|node|developer)\b/.test(prompt)) {
        category = "coding";
    } else if (/\b(business|marketing|product|sell|startup|customer|growth|monetize|revenue|strategy|cost|company|pitch)\b/.test(prompt)) {
        category = "business";
    } else if (/\b(write|poem|story|article|blog|essay|resume|email|copywriting|creative|character|novel)\b/.test(prompt)) {
        category = "creative";
    } else if (/\b(science|math|physics|chemical|formula|space|universe|biology|theory|research|gravity)\b/.test(prompt)) {
        category = "science";
    }

    // Extract key nouns from prompt to make the response highly specific
    const stopwords = new Set(["the", "a", "an", "and", "or", "but", "if", "then", "of", "to", "for", "with", "in", "on", "at", "by", "how", "what", "why", "where", "make", "create", "build", "write", "about", "your", "my", "me", "our", "us"]);
    const cleanedWords = userPrompt
        .replace(/[^\w\s]/g, '')
        .split(/\s+/)
        .filter(w => w.length > 2 && !stopwords.has(w.toLowerCase()));
    
    // Pick top 3-4 keywords
    const keywords = [...new Set(cleanedWords)].slice(0, 4);
    const keyTerm = keywords.join(" ") || "this problem";
    const primeTerm = keywords[0] || "solution";

    // 2. Generate agent-specific content based on role and category
    switch (agent.id) {
        case "agent-architect":
            return getArchitectSimulation(category, keyTerm, primeTerm, keywords);
        case "agent-critic":
            return getCriticSimulation(category, keyTerm, primeTerm, keywords, contextHistory);
        case "agent-search":
            return getSearchSimulation(category, keyTerm, primeTerm, keywords);
        case "agent-engineer":
            return getEngineerSimulation(category, keyTerm, primeTerm, keywords, contextHistory);
        case "agent-synthesizer":
            return getSynthesizerSimulation(category, keyTerm, primeTerm, keywords, contextHistory);
        default:
            return `### Analysis on ${keyTerm}\n\nI have reviewed the problem. Here are my thoughts:\n1. We need to define clear boundaries for ${keyTerm}.\n2. We must align this with standard best practices.\n3. Implementation details should prioritize efficiency. Let's see what the other agents think.`;
    }
}

